    private void PopulateStatusButton()
    {
        StatusButton.Clear();
        _statusAvailability.Clear();

        var canNormal = _whitelistSystem.IsWhitelisted("STHunter");
        var canCouncil = _whitelistSystem.IsWhitelisted("STHunterCouncil");
        var canLeader = _whitelistSystem.IsWhitelisted("STHunterLeader");

        if (_sponsorsSystem.TryGetInfo(out var sponsorInfo) && sponsorInfo.CanPlayHunter)
        {
            if (sponsorInfo.MaxHunterStatus >= HunterStatus.Normal)
                canNormal = true;
            if (sponsorInfo.MaxHunterStatus >= HunterStatus.Council)
                canCouncil = true;
            if (sponsorInfo.MaxHunterStatus >= HunterStatus.Leader)
                canLeader = true;
        }

        _statusAvailability[(int)HunterStatus.Normal] = canNormal;
        _statusAvailability[(int)HunterStatus.Council] = canCouncil;
        _statusAvailability[(int)HunterStatus.Leader] = canLeader;

        void AddStatusItem(HunterStatus status)
        {
            var id = (int)status;
            var isAvailable = _statusAvailability[id];

            if (!isAvailable)
                return;

            var nameKey = $"st-hunter-status-{status.ToString().ToLower()}";
            var name = Loc.GetString(nameKey);

            StatusButton.AddItem(name, id);
        }

        AddStatusItem(HunterStatus.Normal);
        AddStatusItem(HunterStatus.Council);
        AddStatusItem(HunterStatus.Leader);
    }

    private void OnStatusSelected(OptionButton.ItemSelectedEventArgs args)
    {
        if (_profile == null)
            return;

        StatusButton.SelectId(args.Id);
        _profile.Status = (HunterStatus)args.Id;
        SetDirty();
    }

    private void InitializeVoice()
    {
        _allVoices = _prototypeManager
            .EnumeratePrototypes<TTSVoicePrototype>()
            .Where(v => v.RoundStart)
            .OrderBy(v => v.Name)
            .ToList();

        VoiceButton.OnItemSelected += args =>
        {
            VoiceButton.SelectId(args.Id);
            if (_profile != null && args.Id >= 0 && args.Id < _allVoices.Count)
            {
                var voiceProto = _allVoices[args.Id];
                if (voiceProto != null)
                    _profile.Voice = voiceProto.ID;
            }

            SetDirty();
        };

        VoicePlayButton.OnPressed += _ =>
        {
            if (_profile == null)
                return;
            var sys = _entityManager.System<TTSSystem>();
            sys.RequestPreviewTTS(_profile.Voice, true);
        };
    }

    private void UpdateVoiceSelection()
    {
        if (_profile == null)
            return;

        VoiceButton.Clear();
        var sex = _profile.Gender == Gender.Male ? Sex.Male : Sex.Female;
        var selectedListIndex = -1;

        for (var i = 0; i < _allVoices.Count; i++)
        {
            var v = _allVoices[i];

            if (v.Blacklist != null && v.Blacklist.Contains("Hunter"))
                continue;

            if (v.Sex == sex || v.Sex == Sex.Unsexed)
            {
                VoiceButton.AddItem(v.Name, i);
                if (v.ID == _profile.Voice)
                    selectedListIndex = i;
            }
        }

        if (selectedListIndex == -1)
        {
            for (var i = 0; i < _allVoices.Count; i++)
            {
                var v = _allVoices[i];
                var blocked = v.Blacklist != null && v.Blacklist.Contains("Hunter");
                if (!blocked && (v.Sex == sex || v.Sex == Sex.Unsexed))
                {
                    _profile.Voice = v.ID;
                    selectedListIndex = i;
                    break;
                }
            }
        }

        if (selectedListIndex != -1)
            VoiceButton.SelectId(selectedListIndex);
    }

    private Direction RotateDirection(Direction dir, bool clockwise)
    {
        var d = (int)dir;
        if (clockwise)
            d = (d + 2) % 8;
        else
            d = (d - 2 + 8) % 8;

        return (Direction)d;
    }

    private void InitializeCustomizationTabs()
    {
        EquipmentTabs.SetTabTitle(0, Loc.GetString("st-hunter-profile-editor-armor-tab"));
        EquipmentTabs.SetTabTitle(1, Loc.GetString("st-hunter-profile-editor-mask-tab"));
        EquipmentTabs.SetTabTitle(2, Loc.GetString("st-hunter-profile-editor-accessory-tab"));
        EquipmentTabs.SetTabTitle(3, Loc.GetString("st-hunter-profile-editor-greaves-tab"));
        EquipmentTabs.SetTabTitle(4, Loc.GetString("st-hunter-profile-editor-cape-tab"));
        EquipmentTabs.SetTabTitle(5, Loc.GetString("st-hunter-profile-editor-caster-tab"));
        EquipmentTabs.SetTabTitle(6, Loc.GetString("st-hunter-profile-editor-bracer-tab"));

        var quillPicker = new SingleMarkingPicker { Category = MarkingCategories.Hair };
        quillPicker.OnMarkingSelect += OnQuillSelected;
        quillPicker.OnColorChanged += _ => UpdatePreview();
        QuillsContent.AddChild(quillPicker);

        var gearTabs = new Dictionary<string, Control>
        {
            { "Armor", ArmorTab },
            { "Mask", MaskTab },
            { "Accessory", AccessoryTab },
            { "Greaves", GreavesTab },
            { "Cape", CapeTab },
            { "Caster", CasterTab },
            { "Bracer", BracerTab }
        };

        var isSponsor = _sponsorsSystem.TryGetInfo(out var sponsorInfo) && sponsorInfo.CanUseHunterCustomization;

        _categorizedGear.Clear();
        foreach (var gearProto in _prototypeManager.EnumeratePrototypes<HunterGearPrototype>())
        {
            if (!_categorizedGear.ContainsKey(gearProto.Category))
                _categorizedGear[gearProto.Category] = new Dictionary<string, List<HunterGearPrototype>>();

            if (!_categorizedGear[gearProto.Category].ContainsKey(gearProto.Material))
                _categorizedGear[gearProto.Category][gearProto.Material] = new List<HunterGearPrototype>();

            _categorizedGear[gearProto.Category][gearProto.Material].Add(gearProto);
        }

        foreach (var (category, materials) in _categorizedGear)
        {
            if (!gearTabs.TryGetValue(category, out var tab))
                continue;

            tab.DisposeAllChildren();

            if (materials.Count > 1)
            {
                var splitContainer = new SplitContainer
                {
                    Orientation = SplitContainer.SplitOrientation.Horizontal,
                    VerticalExpand = true,
                    HorizontalExpand = true,
                    State = SplitContainer.SplitState.Auto,
                };

                var materialListScroll = new ScrollContainer
                {
                    VerticalExpand = true,
                    HorizontalExpand = false,
                    MinWidth = 120,
                    HScrollEnabled = false
                };
                var materialListContainer = new BoxContainer
                {
                    Orientation = BoxContainer.LayoutOrientation.Vertical,
                    VerticalExpand = true,
                    Margin = new Thickness(0, 0, 5, 0)
                };
                materialListScroll.AddChild(materialListContainer);

                var pickerArea = new BoxContainer
                {
                    Orientation = BoxContainer.LayoutOrientation.Vertical,
                    VerticalExpand = true,
                    HorizontalExpand = true
                };

                void PopulatePicker(string materialName)
                {
                    pickerArea.DisposeAllChildren();
                    if (!materials.TryGetValue(materialName, out var gears)) return;

                    var picker = new SpritePicker();
                    picker.Populate(gears, isSponsor);

                    if (category == "Accessory")
                        picker.AddCustomButton("Nothing", Loc.GetString("st-hunter-gear-none"), false, true);

                    picker.OnSelectionChanged += protoId => OnSpriteSelected(category, protoId);
                    pickerArea.AddChild(picker);
                }

                var materialButtonGroup = new ButtonGroup();
                string? firstMaterial = null;

                foreach (var material in materials.Keys.OrderBy(k => k))
                {
                    firstMaterial ??= material;
                    
                    var matKey = $"st-hunter-material-{material.ToLowerInvariant()}";
                    var locTitle = Loc.GetString(matKey);
                    if (locTitle.StartsWith("st-hunter-material-"))
                        locTitle = material;

                    var btn = new Button
                    {
                        Text = locTitle,
                        ToggleMode = true,
                        Group = materialButtonGroup,
                        TextAlign = Label.AlignMode.Left
                    };
                    btn.OnPressed += _ => PopulatePicker(material);
                    materialListContainer.AddChild(btn);
                }

                splitContainer.AddChild(materialListScroll);
                splitContainer.AddChild(pickerArea);
                tab.AddChild(splitContainer);

                if (firstMaterial != null)
                {
                    PopulatePicker(firstMaterial);
                    if (materialListContainer.ChildCount > 0 && materialListContainer.GetChild(0) is Button firstBtn)
                        firstBtn.Pressed = true;
                }
            }
            else if (materials.Count == 1)
            {
                var gears = materials.First().Value;
                var picker = new SpritePicker();
                
                picker.Populate(gears, isSponsor);

                if (category == "Accessory")
                {
                    picker.AddCustomButton("Nothing", Loc.GetString("st-hunter-gear-none"), false, true);
                }

                picker.OnSelectionChanged += protoId => OnSpriteSelected(category, protoId);
                tab.AddChild(picker);
            }
        }
    }

    private void RandomizeName()
    {
        if (_profile == null)
            return;

        var currentDesc = _cachedFlavorText;
        _profile.Name = HumanoidCharacterProfile.GetName("STHunter", _profile.Gender);
        UpdateControls();
        FlavorTextEdit.TextRope = new Rope.Leaf(currentDesc);
        SetDirty();
    }

    private void RandomizeFull()
    {
        if (_profile == null)
            return;

        var randomProfile = HunterProfile.Random(_prototypeManager, _random);
        var name = HumanoidCharacterProfile.GetName("STHunter", randomProfile.Gender);
        var currentDesc = _cachedFlavorText;

        _profile.Name = name;
        _profile.Gender = randomProfile.Gender;
        _profile.Age = randomProfile.Age;
        
        _profile.ArmorPrototype = randomProfile.ArmorPrototype;
        _profile.MaskPrototype = randomProfile.MaskPrototype;
        _profile.GreavesPrototype = randomProfile.GreavesPrototype;
        _profile.CasterPrototype = randomProfile.CasterPrototype;
        _profile.BracerPrototype = randomProfile.BracerPrototype;
        _profile.HeadAccessory = randomProfile.HeadAccessory;
        
        _profile.SkinColor = randomProfile.SkinColor;
        _profile.Voice = randomProfile.Voice;
        _profile.TranslatorSound = randomProfile.TranslatorSound;
        _profile.CloakSound = randomProfile.CloakSound;
        _profile.CapeColor = randomProfile.CapeColor;

        UpdateControls();
        FlavorTextEdit.TextRope = new Rope.Leaf(currentDesc);

        SetDirty();
        UpdatePreview();
    }

    private void OnSpriteSelected(string category, EntProtoId protoId)
    {
        if (_profile == null)
            return;

        switch (category)
        {
            case "Armor":
                _profile.ArmorPrototype = protoId;
                break;
            case "Mask":
                _profile.MaskPrototype = protoId;
                break;
            case "Greaves":
                _profile.GreavesPrototype = protoId;
                break;
            case "Caster":
                _profile.CasterPrototype = protoId;
                break;
            case "Bracer":
                _profile.BracerPrototype = protoId;
                break;
            case "Accessory":
                _profile.HeadAccessory = protoId;
                break;
            case "Cape":
                _previewCapePrototype = protoId;
                break;
        }

        SetDirty();
        UpdatePreview();
    }

    private void OnGenderSelected(OptionButton.ItemSelectedEventArgs args)
    {
        if (_profile == null)
            return;
        GenderButton.SelectId(args.Id);
        _profile.Gender = (Gender)args.Id;

        UpdateVoiceSelection();
        SetDirty();
        UpdatePreview();
    }

    private void OnQuillSelected((int slot, string id) marking)
    {
