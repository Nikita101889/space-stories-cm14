        UpdatePreview();
    }

    public void SetProfile(HunterProfile? profile)
    {
        _profile = profile?.Clone();
        PopulateStatusButton();
        UpdateControls();
        UpdatePreview();
        _isDirty = false;
        SaveButton.Disabled = true;
    }

    private void UpdateControls()
    {
        if (_profile == null)
            return;

        NameEdit.Text = _profile.Name;

        var genderId = (int)_profile.Gender;
        var genderValid = false;
        for (var i = 0; i < GenderButton.ItemCount; i++)
        {
            if (GenderButton.GetItemId(i) == genderId)
            {
                genderValid = true;
                break;
            }
        }
        if (!genderValid && GenderButton.ItemCount > 0)
        {
            genderId = GenderButton.GetItemId(0);
            _profile.Gender = (Gender)genderId;
            SetDirty();
        }
        if (genderValid || GenderButton.ItemCount > 0)
            GenderButton.SelectId(genderId);
        
        AgeEdit.Text = _profile.Age.ToString();

        _cachedFlavorText = _profile.FlavorText ?? "";
        FlavorTextEdit.TextRope = new Rope.Leaf(_cachedFlavorText);

        var statusId = (int)_profile.Status;

        if (StatusButton.ItemCount > 0)
        {
            var isStatusInList = false;
            for (int i = 0; i < StatusButton.ItemCount; i++)
            {
                if (StatusButton.GetItemId(i) == statusId)
                {
                    isStatusInList = true;
                    break;
                }
            }

            if (!isStatusInList)
            {
                var fallbackId = (int)HunterStatus.Normal;
                var normalInList = false;
                for (int i = 0; i < StatusButton.ItemCount; i++)
                {
                    if (StatusButton.GetItemId(i) == fallbackId)
                    {
                        normalInList = true;
                        break;
                    }
                }

                if (normalInList)
                    statusId = fallbackId;
                else
                    statusId = StatusButton.GetItemId(0);
            
                _profile.Status = (HunterStatus)statusId;
                SetDirty();
            }

            StatusButton.SelectId(statusId);
        }

        SkinColor.Color = _profile.SkinColor;

        if (QuillsContent.ChildCount > 0 && QuillsContent.GetChild(0) is SingleMarkingPicker quillPicker)
        {
            var hairColors = new List<Color> { _profile.SkinColor };
            string markingId = _profile.QuillMarkingId;
            if (string.IsNullOrEmpty(markingId))
                markingId = "HunterHairStandard";

            var hairMarking = new List<Shared.Humanoid.Markings.Marking> { new(markingId, hairColors) };
            quillPicker.UpdateData(hairMarking, "STHunter", 1);
        }

        UpdateVoiceSelection();

        var translatorId = (int)_profile.TranslatorSound;
        var translatorValid = false;
        for (var i = 0; i < TranslatorStyleButton.ItemCount; i++)
        {
            if (TranslatorStyleButton.GetItemId(i) == translatorId)
            {
                translatorValid = true;
                break;
            }
        }
        if (translatorValid)
            TranslatorStyleButton.SelectId(translatorId);
        else if (TranslatorStyleButton.ItemCount > 0)
            TranslatorStyleButton.SelectId(TranslatorStyleButton.GetItemId(0));

        var cloakId = (int)_profile.CloakSound;
        var cloakValid = false;
        for (var i = 0; i < CloakStyleButton.ItemCount; i++)
        {
            if (CloakStyleButton.GetItemId(i) == cloakId)
            {
                cloakValid = true;
                break;
            }
        }
        if (cloakValid)
            CloakStyleButton.SelectId(cloakId);
        else if (CloakStyleButton.ItemCount > 0)
            CloakStyleButton.SelectId(CloakStyleButton.GetItemId(0));
        
        CapeColorSelector.Color = _profile.CapeColor;

        SetPickerSelected("Armor", _profile.ArmorPrototype);
        SetPickerSelected("Mask", _profile.MaskPrototype);
        SetPickerSelected("Greaves", _profile.GreavesPrototype);
        SetPickerSelected("Caster", _profile.CasterPrototype);
        SetPickerSelected("Bracer", _profile.BracerPrototype);
        SetPickerSelected("Accessory", _profile.HeadAccessory);
        SetPickerSelected("Cape", _previewCapePrototype);
    }

    private void SetPickerSelected(string category, EntProtoId protoId)
    {
        Control? tab = category switch
        {
            "Armor" => ArmorTab,
            "Mask" => MaskTab,
            "Greaves" => GreavesTab,
            "Caster" => CasterTab,
            "Bracer" => BracerTab,
            "Accessory" => AccessoryTab,
            "Cape" => CapeTab,
            _ => null,
        };

        if (tab == null)
            return;

        void SelectInPicker(Control container)
        {
            if (container is SpritePicker picker)
            {
                picker.SetSelected(protoId);
                return;
            }
            if (container is BoxContainer box && box.ChildCount > 0 && box.GetChild(0) is SpritePicker innerPicker)
            {
                innerPicker.SetSelected(protoId);
                return;
            }
        }

        if (tab.ChildCount > 0 && tab.GetChild(0) is SplitContainer split)
        {
            if (!_categorizedGear.ContainsKey(category)) return;

            if (split.ChildCount < 2) return;

            var leftScroll = split.GetChild(0) as ScrollContainer;
            var rightArea = split.GetChild(1) as BoxContainer;

            if (leftScroll?.GetChild(0) is BoxContainer listContainer)
            {
                foreach (var (material, gears) in _categorizedGear[category])
                {
                    if (gears.Any(g => g.EntityProto == protoId))
                    {
                        int buttonIndex = -1;
                        var sortedMaterials = _categorizedGear[category].Keys.OrderBy(k => k).ToList();
                        buttonIndex = sortedMaterials.IndexOf(material);

                        if (buttonIndex >= 0 && buttonIndex < listContainer.ChildCount)
                        {
                            if (listContainer.GetChild(buttonIndex) is Button btn)
                            {
                                btn.Pressed = true;

                                rightArea?.DisposeAllChildren();
                                if (rightArea != null)
                                {
                                    var isSponsor = _sponsorsSystem.TryGetInfo(out var sponsorInfo) && sponsorInfo.CanUseHunterCustomization;
                                    var picker = new SpritePicker();
                                    picker.Populate(gears, isSponsor);
                                    if (category == "Accessory") picker.AddCustomButton("Nothing", Loc.GetString("st-hunter-gear-none"), false, true);
                                    
                                    picker.OnSelectionChanged += pid => OnSpriteSelected(category, pid);
                                    rightArea.AddChild(picker);
                                    picker.SetSelected(protoId);
                                }
                            }
                        }
                        return;
                    }
                }
            }
        }
        else if (tab.ChildCount > 0)
        {
            SelectInPicker(tab.GetChild(0));
        }
    }

    private void UpdatePreview()
    {
        if (_profile == null)
            return;

        _entityManager.DeleteEntity(_previewDummy);
        _previewDummy = _entityManager.SpawnEntity("STMobHunterDummy", MapCoordinates.Nullspace);

        var humanoidSystem = _entityManager.System<HumanoidAppearanceSystem>();
        var handsSystem = _entityManager.System<SharedHandsSystem>();

        var quillMarking = _profile.QuillMarkingId;
        if (!_prototypeManager.HasIndex(quillMarking))
            quillMarking = "HunterHairStandard";

        var appearance = new HumanoidCharacterAppearance(
            quillMarking,
            _profile.SkinColor,
            "NoFacialHair",
            Color.White,
            Color.Black,
            _profile.SkinColor,
            new List<Shared.Humanoid.Markings.Marking>()
        );

        var tempHumanoidProfile = new HumanoidCharacterProfile(
            _profile.Name,
            "",
            "STHunter",
            "",
            _profile.Age,
            _profile.Gender == Gender.Male ? Sex.Male : Sex.Female,
            _profile.Gender,
            appearance,
            SpawnPriorityPreference.None,
            ArmorPreference.Random,
            null,
            new Dictionary<ProtoId<JobPrototype>, JobPriority>(),
            PreferenceUnavailableMode.StayInLobby,
            new HashSet<ProtoId<AntagPrototype>>(),
            new HashSet<ProtoId<TraitPrototype>>(),
            new Dictionary<string, RoleLoadout>(),
            new SharedRMCNamedItems(),
            true,
            "",
            ""
        );

        humanoidSystem.LoadProfile(_previewDummy, tempHumanoidProfile);

        if (ShowClothesButton.Pressed)
        {
            void EquipIfValid(EntProtoId proto, string slot)
            {
                if (
                    !string.IsNullOrEmpty(proto)
                    && proto.Id != "Nothing"
                    && _prototypeManager.HasIndex<EntityPrototype>(proto)
                )
                {
                    _inventory.TryEquip(
                        _previewDummy,
                        _entityManager.SpawnEntity(proto, MapCoordinates.Nullspace),
                        slot,
                        true,
                        true
                    );
                }
            }

            EquipIfValid(_profile.BracerPrototype, "gloves");
            EquipIfValid(_profile.ArmorPrototype, "outerClothing");
            EquipIfValid(_profile.MaskPrototype, "mask");
            EquipIfValid(_profile.GreavesPrototype, "shoes");
            
            if (ShowCapeButton.Pressed)
                EquipIfValid(_previewCapePrototype, "back");

            if (_inventory.TryGetSlotEntity(_previewDummy, "mask", out var maskUid))
            {
                var containerSys = _entityManager.System<SharedContainerSystem>();
                if (
                    _entityManager.TryGetComponent<HelmetAccessoryHolderComponent>(maskUid, out var holder)
                    && containerSys.TryGetContainer(maskUid.Value, "helmet-accessory-container", out var container)
                )
                {
                    if (
                        !string.IsNullOrEmpty(_profile.HeadAccessory)
                        && _profile.HeadAccessory.Id != "Nothing"
                        && _prototypeManager.HasIndex<EntityPrototype>(_profile.HeadAccessory)
                    )
                    {
                        var accessory = _entityManager.SpawnEntity(_profile.HeadAccessory, MapCoordinates.Nullspace);
                        containerSys.Insert(accessory, container);
                    }
                }
            }

            if (ShowCapeButton.Pressed && _inventory.TryGetSlotEntity(_previewDummy, "back", out var capeUid))
            {
                _entityManager
                    .System<SharedAppearanceSystem>()
                    .SetData(capeUid.Value, HunterCapeVisuals.Color, _profile.CapeColor);
            }

            if (
                !string.IsNullOrEmpty(_profile.CasterPrototype)
                && _prototypeManager.HasIndex<EntityPrototype>(_profile.CasterPrototype)
            )
            {
                _entityManager.EnsureComponent<HandsComponent>(_previewDummy);
                var caster = _entityManager.SpawnEntity(_profile.CasterPrototype, MapCoordinates.Nullspace);
                if (!handsSystem.TryPickupAnyHand(_previewDummy, caster, false))
                    _entityManager.DeleteEntity(caster);
            }
        }

        SpriteView.SetEntity(_previewDummy);
        SpriteView.OverrideDirection = _previewRotation;
    }

    private void SetDirty()
    {
        _isDirty = true;
        SaveButton.Disabled = false;
    }

    private void Save()
    {
        if (_profile == null || !_isDirty)
            return;

        _profile.Name = NameEdit.Text;
        _profile.Gender = (Gender)GenderButton.SelectedId;

        if (int.TryParse(AgeEdit.Text, out var age))
            _profile.Age = Math.Clamp(age, 175, 3000);

        _profile.FlavorText = _cachedFlavorText;

        _profile.Status = (HunterStatus)StatusButton.SelectedId;
        _profile.SkinColor = SkinColor.Color;

        _prefsManager.UpdateHunterProfile(_profile.Clone());
        _isDirty = false;
        SaveButton.Disabled = true;
        OnProfileChanged?.Invoke();
    }

    protected override void Dispose(bool disposing)
    {
        base.Dispose(disposing);
        if (disposing)
            _entityManager.DeleteEntity(_previewDummy);
    }
